          mongodb: "MongoDB",
          mysql: "MySQL",
          postgresql: "PostgreSQL",
          postgres: "PostgreSQL",
          astro: "Astro",
          svelte: "Svelte",
          nextjs: "Next.js",
          nuxt: "Nuxt.js",
          express: "Express.js",
          firebase: "Firebase",
          aws: "AWS",
          tailwindcss: "TailwindCSS",
          bootstrap: "Bootstrap",
          sass: "Sass",
          scss: "Sass",
          css3: "CSS",
          html5: "HTML",
          java: "Java",
          kotlin: "Kotlin",
